"""
We can get range of character from 
a sentence by slice syntax
a[initial num : End Number]

"""
a = "Hello I am Iqbal"
print(a[4:12]) 

# if you wanna print 4 to 11 we have to enter (end_number+1)

#printing 1st to last 
print(a[0:16])

#print 5 to last 
print(a[5:])

#print 1st to 5
print(a[:5])

#negative index 
print(a[-5:])